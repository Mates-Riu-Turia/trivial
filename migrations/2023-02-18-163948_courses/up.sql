CREATE TABLE courses (
    id VARCHAR(10) NOT NULL PRIMARY KEY,
    anatomia VARCHAR(100),
    english VARCHAR(100),
    biologia VARCHAR(100),
    castellano VARCHAR(100),
    clasica VARCHAR(100),
    dibuix VARCHAR(100),
    ed_fisica VARCHAR(100),
    filosofia VARCHAR(100),
    fisica_quimica VARCHAR(100),
    frances VARCHAR(100),
    historia VARCHAR(100),
    grec VARCHAR(100),
    informatica VARCHAR(100),
    literatura VARCHAR(100),
    llati VARCHAR(100),
    mates VARCHAR(100),
    musica VARCHAR(100),
    orientacio VARCHAR(100),
    plastica VARCHAR(100),
    religio VARCHAR(100),
    tecnologia VARCHAR(100),
    valencia VARCHAR(100),
    etica VARCHAR(100)
);

